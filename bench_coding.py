#!/usr/bin/env python3
"""CODE quality: function generation (auto-tested by execution) + bug finding.

Completes the quality matrix with a programming dimension - important, because most of
the tested models are code models (qwen-coder, deepseek-coder, devstral). Reasoning
(logic puzzles) is a different dimension; here we check real writing and reading of code.

  python3 bench_coding.py qwen3-coder:30b-fast deepseek-fast gpt-oss-fast devstral-small-2:24b-fast

SECURITY WARNING: this script RUNS (exec) code generated by the model against local
test cases. Run it only on trusted local models. Each function is exec'd in a separate
namespace, but this is still execution of code from an LLM.
"""
import ast
import json
import re
import signal
import sys
from contextlib import contextmanager

from _common import generate, load_prompts


class _Timeout(Exception):
    pass


@contextmanager
def _time_limit(seconds):
    """Hard time limit on execution (SIGALRM). Models sometimes generate code with an
    infinite loop (while True with no exit) - without this, exec/calling the function hangs
    forever at 100% CPU (real case: unsloth-q4xl froze the benchmark for 80+ min of CPU).
    Works in the main thread on Unix. Note: code with a bare 'except:' may catch _Timeout -
    for a plain loop (no except) SIGALRM passes through and is caught higher up."""
    def handler(signum, frame):
        raise _Timeout()
    old = signal.signal(signal.SIGALRM, handler)
    signal.setitimer(signal.ITIMER_REAL, seconds)
    try:
        yield
    finally:
        signal.setitimer(signal.ITIMER_REAL, 0)
        signal.signal(signal.SIGALRM, old)


def extract_code(text):
    """Extracts code from the response: prefers a fenced block with 'def' that parses to AST.

    Accepts any language label (python/Python/python3/py...) case-insensitively
    and validates via ast.parse, so as not to exec prose (codex #8)."""
    blocks = re.findall(r"```[a-zA-Z0-9_+-]*\s*(.*?)```", text, re.DOTALL)
    candidates = blocks if blocks else [text]
    for b in candidates:  # first a block with 'def' that parses
        if "def " in b:
            try:
                ast.parse(b)
                return b
            except SyntaxError:
                continue
    for b in candidates:  # then any parseable one
        try:
            ast.parse(b)
            return b
        except SyntaxError:
            continue
    return candidates[0]


def _norm(x):
    """Canonicalize for comparison: tuples -> lists recursively, so that [(1,6)] == [[1,6]]
    (codex #4 - a semantically correct result in a different shape must not falsely fail)."""
    if isinstance(x, (list, tuple)):
        return [_norm(i) for i in x]
    return x


def run_generated(code, func_name, tests, timeout_s=5):
    """Exec the code and check the function against the cases. Returns (ok, message).

    Every exec and every function call has a hard timeout (a loop in the model's code = a
    failed case, not a frozen benchmark)."""
    ns = {}
    try:
        with _time_limit(timeout_s):
            exec(code, ns)
    except _Timeout:
        return False, f"timeout {timeout_s}s in exec (loop in model code)"
    except Exception as e:
        return False, f"exec error: {type(e).__name__}: {e}"
    fn = ns.get(func_name)
    if not callable(fn):
        return False, f"missing function {func_name}"
    for args, expected in tests:
        try:
            with _time_limit(timeout_s):
                got = fn(*args)
        except _Timeout:
            return False, f"timeout {timeout_s}s on {args} (loop in model code)"
        except Exception as e:
            return False, f"runtime {type(e).__name__} on {args}"
        if _norm(got) != _norm(expected):
            return False, f"{func_name}({args})={got!r}, expected {expected!r}"
    return True, "OK"


def grade_bug(answer, keys):
    a = re.sub(r"[*#`]", " ", answer.lower())
    a = re.sub(r"\s+", " ", a)
    for k in keys:
        if re.search(k.lower(), a):
            return True
    return False


def main():
    args = sys.argv[1:]
    no_think = "--no-think" in args  # disables thinking for thinking-models (qwen3.6, deepseek-r1, north)
    hard = "--hard" in args  # hard LeetCode algorithms (sliding window, histogram, DP)
    expert = "--expert" in args  # nontrivial, edge-case, strict specification (separates models)
    # --think=VALUE: explicit thinking level. gpt-oss CANNOT be disabled (harmony), only low|medium|high;
    # Qwen-distill: false works. Mapping: false->False, none/default->None, rest (low/high)->string.
    think_arg = next((a.split("=", 1)[1] for a in args if a.startswith("--think=")), None)
    if think_arg is not None:
        think = False if think_arg == "false" else (None if think_arg in ("none", "default") else think_arg)
    else:
        think = False if no_think else None
    # --num-predict=N: generation token budget. Default 1500, but verbose models (lots of
    # comments) get cut off at this and the code does not parse (SyntaxError = false negative,
    # NOT a model weakness). Measured: unsloth-q4xl done_reason=length at 1500. A higher budget =
    # fairer to verbose models; models that finish naturally (done=stop) do not change the result.
    np_arg = next((a.split("=", 1)[1] for a in args if a.startswith("--num-predict=")), None)
    gen_np = int(np_arg) if np_arg else 1500
    models = [a for a in args if not a.startswith("--")]
    if not models:
        print("Usage: python3 bench_coding.py [--no-think] [--hard|--expert] [--num-predict=N] MODEL [...]")
        sys.exit(1)
    C = load_prompts()["coding"]
    gen_tasks = C["generate_expert"] if expert else (C["generate_hard"] if hard else C["generate"])
    bug_tasks = C["bugfind"]
    total = len(gen_tasks) + len(bug_tasks)

    results = {}
    for m in models:
        print(f"\n== {m} ==")
        gen_pass, details = 0, []
        print("  [code generation - auto-test]")
        for t in gen_tasks:
            trunc = False
            try:
                r = generate(m, t["prompt"], num_predict=gen_np, think=think)
                # done_reason=='length' = response cut off by the budget (false negative risk)
                trunc = r.get("done_reason") == "length"
                code = extract_code(r.get("response") or "")
                ok, msg = run_generated(code, t["func"], t["tests"])
            except Exception as e:
                ok, msg = False, f"error: {e}"
            if not ok and trunc:
                msg = f"[TRUNCATED num_predict={gen_np}] {msg}"
            gen_pass += 1 if ok else 0
            flag = "OK " if ok else ("TR!" if trunc else "NO ")
            print(f"    {t['func']:<18} {flag}  {('' if ok else msg)[:60]}")
            details.append({"task": t["func"], "ok": ok, "trunc": trunc, "msg": msg})
        bug_pass = 0
        print("  [bug finding]")
        for i, t in enumerate(bug_tasks, 1):
            ans = ""
            try:
                r = generate(m, t["prompt"], num_predict=800, think=think)
                ans = r.get("response") or ""
                ok = grade_bug(ans, t["keys"])
            except Exception:
                ok = False
            bug_pass += 1 if ok else 0
            print(f"    bug {i:<14} {'OK ' if ok else 'NO '}")
            # we save the full response - auto-grade is sometimes unreliable, manual audit afterwards (codex #5)
            details.append({"task": f"bug{i}", "ok": ok, "answer": ans})
        score = gen_pass + bug_pass
        print(f"  SCORE: {score}/{total}  (code {gen_pass}/{len(gen_tasks)}, bugs {bug_pass}/{len(bug_tasks)})")
        results[m] = {"score": score, "max": total, "gen": gen_pass, "bug": bug_pass, "details": details}

    print("\n== SUMMARY (code quality) ==")
    for m, r in results.items():
        print(f"  {m:<30} {r['score']}/{r['max']}  (code {r['gen']}/{len(gen_tasks)}, bugs {r['bug']}/{len(bug_tasks)})")

    with open("results_coding.json", "w") as f:
        json.dump(results, f, ensure_ascii=False, indent=2)
    print("\nSaved: results_coding.json")


if __name__ == "__main__":
    main()
